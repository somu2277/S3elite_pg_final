// 6. Occupancy Report
export const exportOccupancyReport = async (dashboardData) => {
  const { matrixBeds, stats } = dashboardData;
  const workbook = new window.ExcelJS.Workbook();
  
  const floorSheet = workbook.addWorksheet('Floor Occupancy');
  floorSheet.columns = [
    { header: 'Floor', key: 'floor' },
    { header: 'Total Beds', key: 'total' },
    { header: 'Occupied', key: 'occupied' },
    { header: 'Vacant', key: 'vacant' },
    { header: 'Occupancy %', key: 'percent' }
  ];
  
  const roomSheet = workbook.addWorksheet('Room Occupancy');
  roomSheet.columns = [
    { header: 'Floor', key: 'floor' },
    { header: 'Room', key: 'room' },
    { header: 'Total Beds', key: 'total' },
    { header: 'Occupied', key: 'occupied' },
    { header: 'Vacant', key: 'vacant' }
  ];

  const floorMap = {};
  const groupedRooms = groupMatrixBeds(matrixBeds);
  groupedRooms.forEach(room => {
    const f = room.floor || 1;
    if (!floorMap[f]) floorMap[f] = { total: 0, occupied: 0, vacant: 0 };
    const rTotal = room.capacity;
    const rOccupied = room.cots.filter(c => c.occupied).length;
    const rVacant = rTotal - rOccupied;
    
    floorMap[f].total += rTotal;
    floorMap[f].occupied += rOccupied;
    floorMap[f].vacant += rVacant;
    
    roomSheet.addRow({ floor: `Floor ${f}`, room: room.roomNumber, total: rTotal, occupied: rOccupied, vacant: rVacant });
  });

  Object.keys(floorMap).forEach(f => {
    const data = floorMap[f];
    const percent = data.total > 0 ? ((data.occupied / data.total) * 100).toFixed(1) + '%' : '0%';
    floorSheet.addRow({ floor: `Floor ${f}`, total: data.total, occupied: data.occupied, vacant: data.vacant, percent });
  });

  floorSheet.addRow({});
  floorSheet.addRow({ floor: 'TOTALS', total: stats?.totalStudents + stats?.vacantBeds, occupied: stats?.totalStudents, vacant: stats?.vacantBeds }).font = { bold: true };

  applyHeaderStyle(floorSheet, 1);
  applyHeaderStyle(roomSheet, 1);
  autoFitColumns(floorSheet);
  autoFitColumns(roomSheet);
  
  await downloadWorkbook(workbook, `Occupancy_Report.xlsx`);
};


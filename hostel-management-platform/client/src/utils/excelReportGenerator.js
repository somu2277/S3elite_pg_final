// Theme Colors
const ORANGE_THEME = 'FFF97316'; // Tailwind orange-500
const RED = 'FFEF4444';
const GREEN = 'FF22C55E';
const YELLOW = 'FFEAB308';

const applyHeaderStyle = (worksheet, headerRowIndex = 1) => {
  const row = worksheet.getRow(headerRowIndex);
  row.font = { bold: true, color: { argb: 'FFFFFFFF' } };
  row.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: ORANGE_THEME } };
  row.alignment = { vertical: 'middle', horizontal: 'center' };
  
  worksheet.autoFilter = {
    from: { row: headerRowIndex, column: 1 },
    to: { row: headerRowIndex, column: worksheet.columns.length }
  };
  
  worksheet.views = [{ state: 'frozen', ySplit: headerRowIndex }];
};

const autoFitColumns = (worksheet) => {
  worksheet.columns.forEach(column => {
    let maxLength = 10;
    column.eachCell({ includeEmpty: true }, cell => {
      const columnLength = cell.value ? cell.value.toString().length : 10;
      if (columnLength > maxLength) maxLength = columnLength;
    });
    column.width = maxLength + 2;
  });
};

const formatCurrency = (worksheet, columnLetters) => {
  columnLetters.forEach(col => {
    worksheet.getColumn(col).numFmt = '₹#,##0.00';
  });
};

const colorCodeStatus = (worksheet, statusColIndex, startRow = 2) => {
  const col = worksheet.getColumn(statusColIndex);
  col.eachCell({ includeEmpty: true }, (cell, rowNumber) => {
    if (rowNumber >= startRow && cell.value) {
      const s = cell.value.toString().toLowerCase();
      let color = null;
      if (s === 'paid' || s === 'approved') color = GREEN;
      else if (s.includes('pending') || s.includes('due') || s.includes('partially')) color = YELLOW;
      else if (s.includes('overdue') || s === 'rejected') color = RED;
      
      if (color) {
        cell.font = { bold: true, color: { argb: color } };
      }
    }
  });
};

const addSummaryTop = (worksheet, stats) => {
  // We'll insert rows at the top for summary
  worksheet.spliceRows(1, 0, 
    ['Total Residents', stats?.totalStudents || 0, '', 'Total PG Revenue', `₹${stats?.expectedRev || 0}`],
    ['Occupied Beds', stats?.occupiedBeds || 0, '', 'Collected Revenue', `₹${stats?.totalCollected || 0}`],
    ['Vacant Beds', stats?.vacantBeds || 0, '', 'Pending Revenue', `₹${stats?.pendingRev || 0}`],
    ['Pending Payments', stats?.pendingPayments || 0, '', 'Total Mess Subscribers', stats?.monthlyMessSubscribers || 0],
    ['Report Generated On', new Date().toLocaleString(), '', 'Generated By', 'Admin'],
    [] // Empty row before data
  );

  // Style summary
  for(let i=1; i<=5; i++) {
    const row = worksheet.getRow(i);
    row.getCell(1).font = { bold: true };
    row.getCell(4).font = { bold: true };
  }
};

const downloadWorkbook = async (workbook, filename) => {
  const buffer = await workbook.xlsx.writeBuffer();
  window.saveAs(new Blob([buffer]), filename);
};


// Helper to group flat beds array into rooms
const groupMatrixBeds = (bedsArray) => {
  if (!bedsArray || bedsArray.length === 0) return [];
  if (bedsArray[0].cots) return bedsArray;
  
  const grouped = bedsArray.reduce((acc, bed) => {
    if (!acc[bed.roomNumber]) {
      acc[bed.roomNumber] = {
        roomNumber: bed.roomNumber,
        floor: bed.floorName || 'Unknown',
        floorName: bed.floorName || 'Unknown',
        cots: []
      };
    }
    acc[bed.roomNumber].cots.push(bed);
    return acc;
  }, {});
  
  const rooms = Object.values(grouped);
  rooms.forEach(r => r.capacity = r.cots.length);
  return rooms;
};

// 1. Export PG Report
export const exportPGReport = async (dashboardData) => {
  const { matrixBeds, stats } = dashboardData;
  const workbook = new window.ExcelJS.Workbook();
  const worksheet = workbook.addWorksheet('PG Residents Data');

  worksheet.columns = [
    { header: 'S.No', key: 'sno' },
    { header: 'Student ID', key: 'studentId' },
    { header: 'Booking ID', key: 'bookingId' },
    { header: 'Student Name', key: 'name' },
    { header: 'Father/Guardian Name', key: 'fatherName' },
    { header: 'Mobile Number', key: 'phone' },
    { header: 'WhatsApp Number', key: 'whatsappNumber' },
    { header: 'Emergency Contact', key: 'emergencyContact' },
    { header: 'Email Address', key: 'email' },
    { header: 'Room Number', key: 'room' },
    { header: 'Cot Number', key: 'cot' },
    { header: 'Floor', key: 'floor' },
    { header: 'Sharing Type', key: 'sharing' },
    { header: 'Monthly Rent', key: 'rent' },
    { header: 'Stay Duration', key: 'duration' },
    { header: 'Expected Joining Date', key: 'joining' },
    { header: 'Booking Date', key: 'bookingDate' },
    { header: 'Booking Status', key: 'bookingStatus' },
    { header: 'Payment Status', key: 'paymentStatus' },
    { header: 'Amount Paid', key: 'paid' },
    { header: 'Amount Pending', key: 'pending' },
    { header: 'Due Date', key: 'dueDate' },
    { header: 'Remarks', key: 'remarks' },
    { header: 'Verified By', key: 'verifiedBy' },
    { header: 'Verification Date', key: 'verificationDate' }
  ];

  let sno = 1;
  let totalRent = 0, totalPaid = 0, totalPending = 0;
  
  const groupedRooms = groupMatrixBeds(matrixBeds);

  groupedRooms.forEach(room => {
    room.cots.forEach(cot => {
      if (cot.occupied && cot.studentName) {
        worksheet.addRow({
          sno: sno++,
          studentId: cot.studentId || 'N/A',
          bookingId: cot.admissionNumber || 'N/A',
          name: cot.studentName,
          fatherName: cot.fatherName || 'N/A',
          phone: cot.phone || 'N/A',
          whatsappNumber: cot.whatsappNumber || 'N/A',
          emergencyContact: cot.emergencyContact || 'N/A',
          email: cot.email || 'N/A',
          room: room.roomNumber,
          cot: cot.bedNumber,
          floor: room.floor,
          sharing: `${room.capacity} Sharing`,
          rent: cot.rentPerBed || 0,
          duration: cot.agreementEndDate ? '11 Months' : 'N/A',
          joining: cot.joiningDate || 'N/A',
          bookingDate: cot.admissionDate || 'N/A',
          bookingStatus: cot.reservationStatus || 'Occupied',
          paymentStatus: cot.paymentStatus || 'Paid',
          paid: (cot.rentPerBed || 0) - (cot.pendingAmount || 0),
          pending: cot.pendingAmount || 0,
          dueDate: cot.nextDueDate || 'N/A',
          remarks: cot.notes || '',
          verifiedBy: 'Admin',
          verificationDate: cot.lastPaymentDate || 'N/A'
        });

        totalRent += cot.rentPerBed || 0;
        totalPending += cot.pendingAmount || 0;
        totalPaid += (cot.rentPerBed || 0) - (cot.pendingAmount || 0);
      }
    });
  });

  worksheet.addRow({});
  worksheet.addRow({
    sharing: 'TOTALS',
    rent: totalRent,
    paid: totalPaid,
    pending: totalPending
  }).font = { bold: true };

  addSummaryTop(worksheet, stats);
  applyHeaderStyle(worksheet, 7);
  autoFitColumns(worksheet);
  formatCurrency(worksheet, ['N', 'T', 'U']); 
  colorCodeStatus(worksheet, 19, 8); 

  const monthYear = new Date().toLocaleString('default', { month: 'long', year: 'numeric' }).replace(' ', '_');
  await downloadWorkbook(workbook, `PG_Report_${monthYear}.xlsx`);
};


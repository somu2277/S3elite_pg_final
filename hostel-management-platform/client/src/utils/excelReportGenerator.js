// 3. Combined Report
export const exportCombinedReport = async (dashboardData) => {
  const workbook = new window.ExcelJS.Workbook();
  const { matrixBeds, messSubscribers, stats } = dashboardData;
  
  // Sheet 1: Summary
  const summarySheet = workbook.addWorksheet('Summary');
  summarySheet.columns = [{ header: 'Metric', key: 'metric' }, { header: 'Value', key: 'val' }];
  summarySheet.addRow({ metric: 'Total Residents', val: stats?.totalStudents || 0 });
  summarySheet.addRow({ metric: 'Occupied Beds', val: stats?.occupiedBeds || 0 });
  summarySheet.addRow({ metric: 'Vacant Beds', val: stats?.vacantBeds || 0 });
  summarySheet.addRow({ metric: 'Total PG Revenue', val: stats?.expectedRev || 0 });
  summarySheet.addRow({ metric: 'Collected Revenue', val: stats?.totalCollected || 0 });
  summarySheet.addRow({ metric: 'Pending Revenue', val: stats?.pendingRev || 0 });
  summarySheet.addRow({ metric: 'Total Monthly Mess Subscribers', val: stats?.monthlyMessSubscribers || 0 });
  summarySheet.addRow({ metric: 'Report Generated On', val: new Date().toLocaleString() });
  autoFitColumns(summarySheet);
  
  // Sheet 2: Residents
  const resSheet = workbook.addWorksheet('PG Residents');
  resSheet.columns = [
    { header: 'Name', key: 'name' },
    { header: 'Room', key: 'room' },
    { header: 'Cot', key: 'cot' },
    { header: 'Rent', key: 'rent' },
    { header: 'Pending', key: 'pending' }
  ];
  const groupedRooms = groupMatrixBeds(matrixBeds);
  groupedRooms.forEach(room => {
    room.cots.forEach(cot => {
      if (cot.occupied && cot.studentName) {
        resSheet.addRow({ name: cot.studentName, room: room.roomNumber, cot: cot.bedNumber, rent: cot.rentPerBed, pending: cot.pendingAmount });
      }
    });
  });
  applyHeaderStyle(resSheet, 1);
  autoFitColumns(resSheet);

  // Sheet 3: Mess
  const messSheet = workbook.addWorksheet('Mess Subscribers');
  messSheet.columns = [
    { header: 'Name', key: 'name' },
    { header: 'Phone', key: 'phone' },
    { header: 'Status', key: 'status' }
  ];
  messSubscribers.forEach(sub => messSheet.addRow({ name: sub.name, phone: sub.phone, status: sub.paymentStatus }));
  applyHeaderStyle(messSheet, 1);
  autoFitColumns(messSheet);

  await downloadWorkbook(workbook, `Combined_Report.xlsx`);
};

